@router.post("/drawings", response_model=DrawingAssetResponse, status_code=201)
async def save_drawing(
    data: DrawingAssetCreate,
    auth: GroupContext = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    """Save a freehand drawing for a member."""
    from src.dependencies import resolve_group_id

    member_id = data.member_id or auth.user_id
    group_id = data.group_id or resolve_group_id(None, auth)
    drawing = await service.save_drawing(
        db,
        member_id=member_id,
        group_id=group_id,
        image_url=data.image_url,
    )
    await db.commit()
    await db.refresh(drawing)
    return drawing


