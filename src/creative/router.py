"""Creative module FastAPI router — AI art, stories, stickers, drawings."""

from uuid import UUID

import structlog
from fastapi import APIRouter, Depends, Query
from sqlalchemy.ext.asyncio import AsyncSession

from src.auth.middleware import get_current_user
from src.billing.feature_gate import check_feature_gate
from src.creative import service
from src.creative.schemas import (
    ArtGenerationCreate,
    ArtGenerationResponse,
    DrawingAssetCreate,
    DrawingAssetResponse,
    StickerPackResponse,
    StickerResponse,
    StoryCreationCreate,
    StoryCreationResponse,
    StoryTemplateResponse,
)
from src.database import get_db
from src.schemas import GroupContext

logger = structlog.get_logger()

router = APIRouter(dependencies=[Depends(check_feature_gate("creative_tools"))])


# ---------------------------------------------------------------------------
# Art generation
# ---------------------------------------------------------------------------


@router.post("/art", response_model=ArtGenerationResponse, status_code=201)
async def generate_art(
    data: ArtGenerationCreate,
    auth: GroupContext = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    """Generate AI art for a member."""
    art = await service.generate_art(
        db,
        member_id=data.member_id,
        group_id=data.group_id,
        prompt=data.prompt,
        model=data.model,
    )
    await db.commit()
    await db.refresh(art)
    return art


@router.get("/art/{member_id}", response_model=list[ArtGenerationResponse])
async def list_member_art(
    member_id: UUID,
    offset: int = Query(default=0, ge=0),
    limit: int = Query(default=50, ge=1, le=100),
    auth: GroupContext = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    """List AI art generated by a member."""
    return await service.list_member_art(db, member_id, offset=offset, limit=limit)


